// Explanation of Grievance ID System
const axios = require('axios');

const API_BASE = 'http://localhost:5004/api';

async function explainGrievanceIds() {
  console.log('GRIEVANCE ID SYSTEM EXPLANATION');
  console.log('==================================');
  console.log('');
  
  try {
    // Get all grievances to show the ID system
    const response = await axios.get(`${API_BASE}/admin/grievances`);
    const grievances = response.data.data;
    
    console.log('GRIEVANCE ID SYSTEM:');
    console.log('====================');
    console.log('');
    
    grievances.forEach((grievance, index) => {
      console.log(`${index + 1}. Grievance Details:`);
      console.log(`   Handle: @${grievance.handle}`);
      console.log(`   Case ID: ${grievance.caseId}`);
      console.log(`   Database ID (_id): ${grievance._id}`);
      console.log(`   Status: ${grievance.status}`);
      console.log(`   Created: ${grievance.createdAt}`);
      console.log('');
    });
    
    console.log('ID SYSTEM BREAKDOWN:');
    console.log('====================');
    console.log('');
    console.log('1. CASE ID (for users):');
    console.log('   - Format: GRV-123456-ABCDEF');
    console.log('   - Generated when grievance is submitted');
    console.log('   - Used for tracking by users');
    console.log('   - Example: GRV-942582-OQRXBF');
    console.log('');
    console.log('2. DATABASE ID (_id) (for admin):');
    console.log('   - Format: MongoDB ObjectId (24 characters)');
    console.log('   - Auto-generated by MongoDB');
    console.log('   - Used for database operations');
    console.log('   - Example: 69d8949e322481f65ea85a08');
    console.log('');
    console.log('3. HOW UPDATES WORK:');
    console.log('   - Frontend uses: selected._id or selected.id');
    console.log('   - Backend receives: req.params.id');
    console.log('   - Database query: { _id: new mongoose.Types.ObjectId(id) }');
    console.log('');
    console.log('4. ID FORMATS:');
    console.log('   - Case ID: GRV-123456-ABCDEF (user-facing)');
    console.log('   - Database ID: 69d8949e322481f65ea85a08 (internal)');
    console.log('');
    
    // Show which ID is used for updates
    if (grievances.length > 0) {
      const example = grievances[0];
      console.log('UPDATE EXAMPLE:');
      console.log('================');
      console.log('When admin clicks "Mark Reviewed":');
      console.log('');
      console.log('Frontend calls:');
      console.log(`PATCH ${API_BASE}/admin/grievances/${example._id}`);
      console.log('Body: { "status": "reviewed" }');
      console.log('');
      console.log('Backend receives:');
      console.log(`req.params.id = ${example._id}`);
      console.log('req.body.status = "reviewed"');
      console.log('');
      console.log('Database query:');
      console.log(`db.grievances.updateOne({ _id: ObjectId("${example._id}") }, { $set: { status: "reviewed" } })`);
      console.log('');
    }
    
  } catch (error) {
    console.error('Error:', error.message);
  }
}

// Run the explanation
explainGrievanceIds();
